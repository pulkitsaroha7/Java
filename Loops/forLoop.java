public class forLoop {
    public static void main(String[] args) {
    // Syntax for For loop:
    //              for(initialization; condition; updation){
    //                 code
    //  
    for(int i = 1; i<=10 ; i++){
        System.out.println("Hello World " + i);
    }
    System.out.println("Loop execution finished");
    }    
}
