public class doWhile {
    public static void main(String[] args) {
    // Synatx for Do-while:
    //                          do{
    //                              code
    //                          }while(condition)
        int counter = 1;
        do{
            System.out.println("Hello World " + counter);
            counter++;
        }while(counter<=10);

/*IMP*/ counter = 13;
        do{
            System.out.println("Hello World " + counter);
            counter++;
        }while(counter<=10);
    }
}
